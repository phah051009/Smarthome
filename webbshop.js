//Popup fönster för Philips högtalare
function philips() {
    const informationdiv = document.getElementsByClassName("information");
    document.querySelector('body').classList.add('popup-visible-philips');
    document.getElementById("informationphilips").style.display = "block"
}
function closebuttonphilips() {
    const closebutton = document.getElementById("informationphilips").style.display = "none";
    document.querySelector('body').classList.remove('popup-visible-philips');
}
